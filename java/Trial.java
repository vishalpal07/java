class Trial {

    public static void main(String args[]) {
        
        int var1 = 25;
        short var2 = 50;
        long var3 = 90L;

       
        float num1 = 3.2f;
        double num2 = 9.15;

        char ch = 'A';

        boolean value = true;

       
        String data = "Hello World";

        
        System.out.println("Integer Value: " + var1);
        System.out.println("Short Value: " + var2);
        System.out.println("Long Value: " + var3);
        System.out.println("Float Value: " + num1);
        System.out.println("Double Value: " + num2);
        System.out.println("Character Value: " + ch);
        System.out.println("Boolean Value: " + value);
        System.out.println("String Value: " + data);
    }
}
